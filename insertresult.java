    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String rollno=jTextField1.getText();
        String physics=jTextField2.getText();
        String math=jTextField3.getText();
        String EM=jTextField4.getText();
        String FM=jTextField5.getText();
        
        try{
          Class.forName("com.mysql.cj.jdbc.Driver");
          Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srmp","root","Nilesh@231098");
          
          Statement st=con.createStatement();
          ResultSet rs=st.executeQuery("Select * from student where Rollno='"+rollno+"'");
          
          if(rs.next()){
              st.executeUpdate("insert into result(Rollno,Physics,Math,EM,FM)values('"+rollno+"','"+physics+"','"+math+"','"+EM+"','"+FM+"')");
              JOptionPane.showMessageDialog(null,"Result registered successfully!!");
              setVisible(false);
              new insertresult().setVisible(true);
          }
          else{
              JOptionPane.showMessageDialog(null,"Student is not registered!");
          }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

